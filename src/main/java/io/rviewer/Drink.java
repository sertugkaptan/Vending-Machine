package io.rviewer;

public class Drink{
    private String drinkName;
    private Double price;


    public String getDrinkName(){
        return drinkName;
    }

    public void setDrinkName(String newName){
        drinkName = newName;
    }

    public Double getPrice(){
        return price;
    }

    public void setPrice(String newPrice ){
        drinkName = newPrice;
    }
}